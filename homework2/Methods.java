package com.company;

public class Methods {
    public int minNumber(int a, int b) {
        return Math.min(a, b);
    }

    public boolean isEven(int a) {
        return a % 2 == 0;
    }

    public int squared(int a) {
        return a * a;
    }

    public int cubed(int a) {
        return a * a * a;
    }
}
